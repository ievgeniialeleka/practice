package com.example;

public class Calculator {
    public static int getSum(int a, int b) {
        return a + b;
    }
    public static int getDiff(int a, int b) {
        return a - b;
    }
    public static int getProduct(int a, int b) {
        return a * b;
    }
    public static int getQuotient(int a, int b) {
        return a / b;
    }
}
